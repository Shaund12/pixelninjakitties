// DOM elements
const grid = document.getElementById('grid');
const countP = document.getElementById('count');
const dashboard = document.getElementById('dashboard');
const controls = document.getElementById('controls');
const loadingEl = document.getElementById('loading');
const emptyState = document.getElementById('emptyState');

// Connection state
const CONNECTION_KEY = 'ninja_cats_wallet';

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    initializeGallery();
});

// Initialize gallery with connection check
async function initializeGallery() {
    const savedAddress = localStorage.getItem(CONNECTION_KEY);

    if (savedAddress) {
        console.log("Found saved wallet connection:", savedAddress);
        showLoading(true);
        try {
            await loadCats(savedAddress);
        } catch (error) {
            console.error("Error loading cats:", error);
            countP.textContent = "Error loading your cats. Please reconnect your wallet.";
        } finally {
            showLoading(false);
        }
    }

    // Listen for wallet connection events from connect-only.js
    window.addEventListener('walletChanged', (event) => {
        const address = event.detail?.address;
        if (address) {
            showLoading(true);
            loadCats(address).finally(() => showLoading(false));
        } else {
            // Wallet disconnected
            grid.innerHTML = '';
            countP.textContent = "Connect wallet to load your ninja cats...";
            dashboard.style.display = 'none';
            controls.style.display = 'none';
            emptyState.style.display = 'none';
        }
    });

    // For backward compatibility, still handle direct button click
    // This won't conflict because connect-only.js will update the button text
    const connectBtn = document.getElementById('connectBtn');
    if (connectBtn && !connectBtn.hasClickHandler) {
        connectBtn.hasClickHandler = true;
        connectBtn.addEventListener('click', async () => {
            if (!localStorage.getItem(CONNECTION_KEY)) {
                try {
                    const provider = new ethers.BrowserProvider(window.ethereum);
                    const signer = await provider.getSigner();
                    const address = await signer.getAddress();
                    loadCats(address);
                } catch (error) {
                    console.error("Failed to connect wallet directly:", error);
                }
            }
        });
    }
}

// Load cats for a given owner address
async function loadCats(ownerAddress) {
    try {
        grid.innerHTML = ''; // Clear previous

        const provider = new ethers.BrowserProvider(window.ethereum);
        const nft = new ethers.Contract(CONTRACT, ABI, provider);

        // Get balance and update count
        const balance = Number(await nft.balanceOf(ownerAddress));
        countP.textContent = balance + " cats";

        // Show or hide UI based on balance
        if (balance > 0) {
            dashboard.style.display = 'flex';
            controls.style.display = 'flex';
            emptyState.style.display = 'none';

            // Update dashboard stats
            document.getElementById('totalCount').textContent = balance;

            // Load cats
            for (let i = 0; i < balance; i++) {
                const id = (await nft.tokenOfOwnerByIndex(ownerAddress, i)).toString();
                const uri = await nft.tokenURI(id);
                const meta = await (await fetch(uri.replace('ipfs://', 'https://ipfs.io/ipfs/'))).json();
                addTile(id, meta);
            }

            // Calculate rarity stats
            updateRarityStats();
        } else {
            // Show empty state
            dashboard.style.display = 'none';
            controls.style.display = 'none';
            emptyState.style.display = 'block';
        }
    } catch (error) {
        console.error("Error loading cats:", error);
        countP.textContent = "Error loading your cats: " + error.message;
        throw error;
    }
}

// Add a cat tile to the grid
function addTile(id, meta) {
    const rank = rarity(id);
    grid.insertAdjacentHTML('beforeend', `
     <div class="catTile" onclick="location='kitty.html?id=${id}'">
        <img class="pixel" src="${meta.image.replace('ipfs://', 'https://ipfs.io/ipfs/')}" alt="">
        <span>#${id}</span>
        <div class="rarityBar" data-rank="${rank}"></div>
     </div>`);
}

// Determine rarity based on ID
function rarity(id) {
    return id % 100 == 0 ? 'legendary' :
        id % 10 == 0 ? 'epic' :
            id % 2 == 0 ? 'rare' :
                'common';
}

// Show or hide loading state
function showLoading(isLoading) {
    if (isLoading) {
        loadingEl.style.display = 'flex';
    } else {
        loadingEl.style.display = 'none';
    }
}

// Calculate and update rarity stats in the dashboard
function updateRarityStats() {
    // Find the rarest cat
    const catElements = document.querySelectorAll('.rarityBar');
    let rarityLevels = {
        'legendary': 0,
        'epic': 0,
        'rare': 0,
        'common': 0
    };

    catElements.forEach(el => {
        const rank = el.getAttribute('data-rank');
        if (rank) rarityLevels[rank]++;
    });

    // Update rarest cat stat
    if (rarityLevels.legendary > 0) {
        document.getElementById('rarestRarity').textContent = 'Legendary';
    } else if (rarityLevels.epic > 0) {
        document.getElementById('rarestRarity').textContent = 'Epic';
    } else if (rarityLevels.rare > 0) {
        document.getElementById('rarestRarity').textContent = 'Rare';
    } else {
        document.getElementById('rarestRarity').textContent = 'Common';
    }

    // Update breed distribution (would require metadata)
    document.getElementById('breedDistribution').textContent = 'Various';
}

